# What is frozenset?
# frozenset is an immutable version of a set.
# Like sets, it contains unique, unordered, unchangeable elements.
# Unlike sets, elements cannot be added or removed from a frozenset.

# Creating frozenset
x = frozenset({"apple", "mango", "cherry"})
print(x)
print(type(x))


