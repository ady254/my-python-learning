# Join two tuples using + operator
tuple1 = ("a", "b", "c")
tuple2 = (1, 2, 3)

tuple3 = tuple1 + tuple2
print(tuple3)

# Join multiply tuples using * operator
fruits = ("apple", "banana", "cherry")
mytuple = fruits * 3
print(mytuple)