# Upper case: The upper() method returns the string in upper case.
# Example 1:

x = "Hello Adnan"

print(x.upper())

# Output:
# HELLO ADNAN

# Lower case: The lower() method returns the string in lower case.
# Example 2:

x = "Hello Adnan"

print(x.lower())

# Output:
# hello adnan

# Strip: The strip() method removes whitespace from the beginning and end of a string.
# Example 3:

x = "   Hello Adnan   "

print(x.strip())

# Output:
# Hello Adnan

# Replace: The replace() method replaces a specified value with another value in a string.
# Example 4:

x = "Hello Adnan"

print(x.replace("Adnan", "Ahmad"))

# Output:
# Hello Ahmad

# Split: The split() method splits a string into a list.
# Example 5:

x = "Hello Adnan"

print(x.split())

# Output:
# ['Hello', 'Adnan']

# Capitalize: The capitalize() method returns the string in capitalize case.
# Example 6:

x = "Hello Adnan"

print(x.capitalize())

# Output:
# Hello adnan