# What is String?
# Ans: String is a sequence of characters.

# How to create a string?
# Ans: String can be created using single quotes, double quotes, or triple quotes.

# Example:

x = "Hello World"
y = 'Hello World'
z = """Hello World"""

print(x)
print(y)
print(z)

# Output:
# Hello World
# Hello World
# Hello World






