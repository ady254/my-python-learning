# Casting
# Casting is the process of converting one data type to another.

# Example:

x = int(1)
y = float(2)
z = complex(3)

print(x)
print(y)
print(z)

print(type(x))
print(type(y))
print(type(z))

# Output:
# 1
# 2.0
# (3+0j)
# <class 'int'>
# <class 'float'>
# <class 'complex'>

