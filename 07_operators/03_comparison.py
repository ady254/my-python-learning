# Comparison Operators: are used to compare two values and return a boolean value (True or False).  

# Types of comparison operators:
# 1. Equal to (==)
# 2. Not equal to (!=)
# 3. Greater than (>)
# 4. Less than (<)
# 5. Greater than or equal to (>=)
# 6. Less than or equal to (<=)

# Example:
x = 5
y = 10
print(x == y)  # False
print(x != y)  # True
print(x > y)   # False
print(x < y)   # True
print(x >= y)  # False
print(x <= y)  # True