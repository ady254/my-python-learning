# Logical Operators: are used to combine two or more conditions and return a boolean value (True or False).

# Types of logical operators:
# 1. AND (and)
# 2. OR (or)
# 3. NOT (not)

# Example:
x = 5
y = 10
print(x and y)  # True
print(x or y)   # True
print(not x)    # False