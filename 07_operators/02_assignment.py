# What is assignment operators?
# Assignment operators are used to assign values to variables

# Example:
x = 5
print(x)

# Output:
# 5
# Here = to means- variable x is assigned the value 5

# Types of assignment operators:
# 1. Simple Assignment Operator
# 2. Chained Assignment Operator
# 3. Augmented Assignment Operator

# 1. Simple Assignment Operator
# Example:
x = 5
print(x)

# Output:
# 5
# Here = to means- variable x is assigned the value 5

# 2. Chained Assignment Operator
# Example:
x = y = z = 5
print(x, y, z)

# Output:
# 5 5 5
# Here = to means- variable x, y, z are assigned the value 5

# 3. Augmented Assignment Operator
# Example:
x = 5
x += 5
print(x)

# Output:
# 10
# Here += to means- variable x is assigned the value 5 and then added to 5

